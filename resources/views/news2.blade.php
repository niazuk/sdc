							<h5>When Should Freelancers Use a Collections Agency?</h5>
							<p><b>Freelance work is becoming more common, with many pursuing it as a full-time career. But with the nature of this industry, there can be downsides, some as serious as not getting paid for your work.<br><br>

							For those who count on their freelance paychecks to pay the rent or cover necessities each month, missing even just one payment could be detrimental to their financial health. Missing multiple payments, however, could harm one's credit score and negatively affect cash flow.<br><br>

							Thankfully, there are collection agencies for when these issues get out of hand. These debt collectors, however, should not be a first-resort solution. Hiring a collections agency will likely result in the loss of your client, and there are many risks and downsides of doing so.<br><br>

							Unless you've hit a wall with a non-paying client and have the law on your side, you should wait until you are certain that a collections agency is your only hope. Here's how to know you've reached that point.</b></p><br>

							<h3><b>Your payment plan was breached.</b></h3><br>

							<p><b>Before agreeing to do freelance work, set up a payment plan to ensure you and your client are on the same page. That way, if the plan is violated, you have grounds to hire a collections agency and fight for your money.<br><br>

							L. Burke Files, CACM, DDP, president of Financial Examinations and Evaluations Inc., said you should consider choosing an agency if "you have a clear written agreement, including the choice of law for settling disputes." Any legal document that was signed, then broken, by a client will help back your case to collect any debts.</b></p>

							<h3><b>You've spent over 90 days waiting for payment.</b></h3><br>

							<p><b>If you feel you are wasting too much time waiting, don't hesitate to seek help, said Files.<br><br>

							Specifically, if an unpaid invoice reaches the 90-day mark, act immediately. After that point, the probability of collecting that debt only decreases. It's typically a "last straw" for most freelancers.</b></p>

							<h3><b>The unpaid invoices are worth more than the amount you're paying an agency.</b></h3><br>

							<p><b>Don't fret over an invoice that's only a few hundred dollars. Sure, you shouldn't let it slide any time your clients fail to pay you, but you must weigh your options.<br><br>

							According to Investopedia, collection agencies typically charge 25 to 45 percent of the amount collected. Ask yourself if it's worth investing in an agency that will take a percentage of your debt. For large invoices, the answer is probably yes. For smaller ones, you might want to simply move forward and no longer do business with that client.</b></p>

							<h3><b>Your client has a history of not paying.</b></h3><br>

							<p><b>If the person you're working with has done this before, consider enlisting the help of an agency. Odds are, there's a pattern to their behavior, and you shouldn't have to continue dealing with it.<br><br>

							But, if this is their first offense, speak with them about it before seeking outside help. Give them time and hear them out. You don't want to lose business because of a simple miscommunication.<br><br>

							Source: Business News Daily.com(Sammi Caramela, Staff Writer)<br> Published: August 30 2018</b></p>
